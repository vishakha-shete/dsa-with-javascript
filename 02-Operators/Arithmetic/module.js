// Modulus operator examples
console.log(10 % 2);  // 0
console.log(10 % 3);  // 1
console.log(2 % 10);  // 2

// Removing last digit
let n = 45678;
console.log(Math.floor(n / 10));    // 4567
console.log(Math.floor(n / 100));   // 456
console.log(Math.floor(n / 1000));  // 45
console.log(Math.floor(n / 10000)); // 4

// Getting last digit
console.log(n % 10); // 8
