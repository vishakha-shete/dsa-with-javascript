// They always return a Boolean value (true or false)
// Comparison operators always return boolean values

console.log(10 > 9);    // true
console.log(10 >= 9);   // true
console.log(10 < 9);    // false
console.log(10 <= 9);   // false
console.log(9 >= 9);    // true

console.log(9 == 9);        // true
console.log('9' == 9);      // true (type coercion)
console.log('9' === 9);     // false (strict check)

console.log('9' != 9);      // false
console.log('9' != 10);     // true
